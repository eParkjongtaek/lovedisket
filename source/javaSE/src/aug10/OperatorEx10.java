package aug10;

class OperatorEx10 
{ 
      public static void main(String[] args) 
      { 
            int a = 1000000;                  // 1,000,000   1백만 
            int b = 2000000;                  // 2,000,000   2백만 
            int c = a * b;                   // 2,000,000,000,000
            System.out.println(c); 
      } 
} 